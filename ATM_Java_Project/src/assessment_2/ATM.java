    private void jButton17ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton17ActionPerformed
        if (action == 1) {          //if action number is 1, then withdraw
            String s = t2.getText();       //get user's input number
            double s2 = Double.valueOf(s);      //convert into double

            if (balance < s2) {        //if withdraw amount greater than balance, print error message
                t1.append("\nSorry!\nYour balance is not enough,\nplease try again.\n");
            } else {
                balance -= s2;      //do withdraw
                t1.append("\nSuccess!\nYour new banlance is: \n" + balance
                        + "\nClick BACK to go back\n");
            }
        } else if (action == 2) {   //if action number is 2, then deposite
            String s = t2.getText();    //get user's input number
            double s2 = Double.valueOf(s);      //convert into double
            balance += s2;      //do deposit
            t1.append("\nSuccess!\nYour new banlance is: \n" + balance
                    + "\nClick BACK to go back\n");
        }
    }//GEN-LAST:event_jButton17ActionPerformed

